<?php
	function demo($n, $m) {
		$i = 0;
		$j = 0;
		
		echo "Starting demo <br>";
		
		for ($i=1; $i <= $n; $i++) {
			echo $i . "--> ";
			for ($j=1; $j <= $m; $j++) {
				echo $j. " ";
			}
			echo "<br>";
		}
	}
	
	// 
	echo "demo(3,3) ---------------------------------------------------------------- <br>";
	demo(3,3);
	
		// 
	echo "demo(2,4) ---------------------------------------------------------------- <br>";
	demo(2,4);
?>