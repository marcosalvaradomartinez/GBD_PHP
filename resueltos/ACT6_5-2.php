<?php
	function demo($var) {
		$i = $var;
		
		echo "Starting demo <br>";
		
		while( $i>=0 ) {
			echo $i . "<br>";   
			$i--;
		}
	}
	// 
	echo "demo(1) ---------------------------------------------------------------- <br>";
	demo(1);

	// 
	echo "demo(8) ---------------------------------------------------------------- <br>";
	demo(8);

?>