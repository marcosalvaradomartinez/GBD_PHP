<?php
	function demo($n, $m) {
		$i = 1;
		$j = 1;
		
		echo "Starting demo <br>";
		
		while ($i <= $n) {
			echo $i . "--> ";
			$j = 1;
			while ($j <= $m) {
				echo $j. " ";
				$j++;
			}
			$i++;
			echo "<br>";
		}
	}
	
	// 
	echo "demo(3,3) ---------------------------------------------------------------- <br>";
	demo(3,3);
	
		// 
	echo "demo(2,4) ---------------------------------------------------------------- <br>";
	demo(2,4);
?>