<?php
	function demo($n, $m) {
		$i = 0;
		$j = 0;
		
		echo "<h1>Starting demo</h1>";
		
		for ($i=0; $i <= $n; $i++) {
			echo '<tr>';
			for ($j=1; $j <= $m; $j++) {
				if ($i == 0) {
					echo '<b><u>' . $i . '</b></u>';
				} else {
					echo '<i>' . $j. '</i>';
				}
			}
			echo '<br>';
		}
	}
	
	// 
	echo "demo(3,3) ---------------------------------------------------------------- <br>";
	demo(3,3);
	
	// 
	echo "demo(2,4) ---------------------------------------------------------------- <br>";
	demo(2,4);
?>