<?php
	function demo($var1, $var2, $var3) {
		$i = 0;
		
		echo "Starting demo <br>";
		
		for( $i=$var1; $i <= $var2; $i++ ) {
			if ($i <= $var3) {
				echo $i . "<br>";
			}
		}
	}
	// 
	echo "demo(1, 1, 0) ---------------------------------------------------------------- <br>";
	demo(1, 1, 0);

	// 
	echo "demo(3, 5, 2) ---------------------------------------------------------------- <br>";
	demo(3, 5, 2);
	
	// 
	echo "demo(5, 10, 7) ---------------------------------------------------------------- <br>";
	demo(5, 10, 7);

?>